package models;

/**
 *
 * @author luis
 */
public class Publisher {
    
    private String name;
    private int code;

    public Publisher() {
    }

    public Publisher(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
    
    @Override
    public String toString() {
        return name;
    }
}
